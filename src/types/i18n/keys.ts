// --- Conditional Mapping Type --- //

// Maps a TranslationKey to its specific parameter type
export type KeyToParams<K extends TranslationKey> = K extends 'errors.requiredFieldsMissing'
  ? FieldParams
  : K extends 'auth.signUp.errorPasswordTooShort' | 'auth.signUp.passwordHint'
    ? MinParams
    : K extends 'common.error.generic' | 'common.error.failedToLoadData'
      ? NoParams
      : K extends
          | 'customers.createError'
          | 'customers.deleteError'
          | 'products.deleteError'
          | 'quotes.deleteError'
          | 'settings.saveError'
          | 'settings.fetchError'
          | 'categories.createError'
          | 'categories.updateError'
          | 'account.profile.updateError'
          | 'account.security.passwordChangeError'
          | 'account.info.updateError'
          | 'account.info.errorConflict'
      ? MessageParams
      : K extends 'quotes.placeholders.title'
        ? IdParams
        : K extends 'settings.changeThemeTitle'
          ? ThemeParams
          : K extends 'print.document'
            ? DateParams
            : K extends 'quoteSummary.grandTotal'
              ? PercentParams
              : K extends 'dashboard.recentQuotes.revenueInfo'
                ? CountParams
                : K extends
                      | 'categories.edit.pageTitle'
                      | 'customers.edit.pageTitle'
                      | 'products.edit.pageTitle'
                  ? { name: string }
                  : K extends 'settings.fetchErrorDetail'
                    ? SettingsFetchErrorDetailParams
                    : NoParams;

// --- Helper Types for Overloading --- //

// Extracts keys that require specific parameters (i.e., KeyToParams<K> is not NoParams)
export type KeysWithParams = {
  [K in TranslationKey]: KeyToParams<K> extends NoParams ? never : K;
}[TranslationKey];

// Extracts keys that do not require specific parameters (i.e., KeyToParams<K> is NoParams)
export type KeysWithoutParams = {
  [K in TranslationKey]: KeyToParams<K> extends NoParams ? K : never;
}[TranslationKey];

